#define __SFR_OFFSET 0
#include "avr/io.h"
.global hwSetup

hwSetup:
SBI DDRB, 5

main:
LDI R22, 0
STS 0x200, R22
LDI R27, 10
STS 0x204, R27
LDI R30, 0
STS 0x200, R30
RJMP for_1
endfor_1:
LDS R20, 0x204
LDI R28, 85
CP R20, R28
BREQ if_1
RJMP else_1
endif_1:
RET

for_1:
LDS R20, 0x200
LDI R18, 10
CP R20, R18
BRGE endfor_1
LDS R21, 0x204
LDS R22, 0x200
ADD R21, R22
LDI R22, 2
ADD R21, R22
STS 0x204, R21
LDS R23, 0x200
LDI R30, 1
ADD R23, R30
STS 0x200, R23
RJMP for_1

if_1:
SBI PORTB, 5
RJMP endif_1

else_1:
CBI PORTB, 5
RJMP endif_1
