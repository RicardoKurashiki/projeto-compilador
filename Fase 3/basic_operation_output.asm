#define __SFR_OFFSET 0
#include "avr/io.h"
.global hwSetup

hwSetup:
SBI DDRB, 5
CBI DDRD, 2
main:
LDI R19, 5
STS 0x100, R19
LDI R26, 2
STS 0x104, R26
LDS R18, 0x100
LDS R21, 0x104
ADD R18, R21
STS 0x108, R18
LDS R16, 0x100
LDS R23, 0x104
SUB R16, R23
STS 0x108, R16
SBIS PIND, 2
LDI R24, 0
SBIC PIND, 2
LDI R24, 1
STS 0x108, R24
SBI PORTB, 5
RET
